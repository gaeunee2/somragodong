export async function generateDailyFortune(): Promise<string> {
  // 날짜를 기반으로 항상 같은 운세가 나오도록 시드 생성
  const today = new Date();
  const dateString = `${today.getFullYear()}-${today.getMonth() + 1}-${today.getDate()}`;
  
  // 날짜 문자열을 기반으로 해시 생성 (간단한 해시 함수)
  let hash = 0;
  for (let i = 0; i < dateString.length; i++) {
    const char = dateString.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // 32비트 정수로 변환
  }
  
  // 해시를 사용하여 운세 선택 (같은 날은 항상 같은 운세)
  const index = Math.abs(hash) % dailyFortunes.length;
  
  // 간단한 지연시간 추가
  await new Promise(resolve => setTimeout(resolve, 100 + Math.random() * 300));
  
  return dailyFortunes[index];
}
